def menu_keyboard_maker():
    keyboard_builder = ReplyKeyboardBuilder()
    button1 = KeyboardButton(text=user_keyboard_button['new_alarm'])
    button2 = KeyboardButton(text=user_keyboard_button['recomend'])
    web_app_button = KeyboardButton(
        text="üìã–û –±–æ—Ç–µüìã",
        web_app=WebAppInfo(url="https://sleepingbot.bitrix24site.ru")
    )
    keyboard_builder.add(web_app_button)
    keyboard_builder.row(button1)
    keyboard_builder.row(button2)
    return keyboard_builder.as_markup(resize_keyboard=True)

def faq_keyboard_maker():
    builder = InlineKeyboardBuilder()
    for faq in faqs.keys():
        button = InlineKeyboardButton(text=faqs[faq], callback_data=faq)
        builder.row(button)
    return builder.as_markup(resize_keyboard=True, is_persistent=True)
